    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txt_nama.getText().equals("")) {
            jLabel2.setText("Error : Harap isi semua kolom.");
        } else {
                if (jButton2.getText().equals("Simpan Meja")) {
                    tambahMeja();
                    txt_nama.setText("");
                    txt_id.setText("");
                } else {
                    int n = jOptionPane1.showConfirmDialog(
                            this, "Apakah anda yakin ingin mengubah meja dengan id " + txt_id.getText() + " ?",
                            "Ubah Meja", JOptionPane.YES_NO_OPTION);
                    if (n == JOptionPane.YES_OPTION) {
                        editMeja();
                        txt_nama.setText("");
                        txt_id.setText("");
                    }
                }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

