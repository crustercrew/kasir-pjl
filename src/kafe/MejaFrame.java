    private void ambilMeja() {
        try {
            Class.forName(driver);
            Connection connection = DriverManager.getConnection(url, user, pass);
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM meja";
            ResultSet rs = statement.executeQuery(sql);

            jTable1.getSelectionModel().addListSelectionListener(
                    new ListSelectionListener() {
                public void valueChanged(ListSelectionEvent event) {
                    int viewRow = jTable1.getSelectedRow();
                    if (viewRow < 0) {
                        //Selection got filtered away.
                    } else {
                        txt_id.setText("" + jTable1.getValueAt(viewRow, 0));
                        txt_nama.setText("" + jTable1.getValueAt(viewRow, 1));

                        jButton2.setText("Update Meja");
                        jButton1.setEnabled(true);
                    }
                }
            }
            );

            jTable1.setModel(new TableMejaModel1(rs));

            pack();

            rs.close();
            statement.close();
            connection.close();
        } catch (Exception DBException) {
            jLabel2.setText("Error : " + DBException);
        }
    }

    public void tambahMeja() {
        try {
            String nama_meja = txt_nama.getText();

            Class.forName(driver);
            Connection connection = DriverManager.getConnection(url, user, pass);
            Statement statement = connection.createStatement();

            String sql = "INSERT INTO meja (nama_meja) values ('" + nama_meja + "');";
            statement.executeUpdate(sql);

            statement.close();
            connection.close();

            jLabel2.setText("Success: Data telah disimpan");
            jLabel2.setForeground(Color.blue);
            ambilMeja();
        } catch (Exception DBException) {
            jLabel2.setText("Error : " + DBException);
        }
    }

    public void editMeja() {
        try {
            String id_meja = txt_id.getText();
            String nama_meja = txt_nama.getText();

            Class.forName(driver);
            Connection connection = DriverManager.getConnection(url, user, pass);
            Statement statement = connection.createStatement();

            String sql = "UPDATE meja SET nama_meja='" + nama_meja + "' where id_meja='" + id_meja + "'";
            statement.executeUpdate(sql);

            statement.close();
            connection.close();

            jButton2.setText("Simpan Meja");
            jLabel2.setText("Success : Data telah diupdate");
            ambilMeja();
        } catch (Exception DBException) {
            jLabel2.setText("Error : " + DBException);
        }
    }

    public void hapusData() {
        try {
            String id_meja = txt_id.getText();

            Class.forName(driver);
            Connection connection = DriverManager.getConnection(url, user, pass);
            Statement statement = connection.createStatement();

            String sql = "DELETE FROM meja where id_meja='" + id_meja + "'";
            statement.executeUpdate(sql);

            statement.close();
            connection.close();

            jLabel2.setText("Success : Meja telah dihapus");
            jButton2.setText("Simpan Meja");
            ambilMeja();
        } catch (Exception DBException) {
            jLabel2.setText("Error : " + DBException);
        }
    }

